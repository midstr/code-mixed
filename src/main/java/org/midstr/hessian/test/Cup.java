package org.midstr.hessian.test;

import java.io.Serializable;

/**
 * @author yaogangli
 * @date 2013-5-22 下午4:34:17
 */
public class Cup implements Serializable{

	private String id;
	private String name;
	private int type;

	/**
	 * 
	 */
	public Cup(String id) {
		this.id = id;
	}

	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}

	/**
	 * @param id
	 *            the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the type
	 */
	public int getType() {
		return type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(int type) {
		this.type = type;
	}

}
